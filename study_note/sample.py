def sample_add_int(x: int, y: int) -> int:
    """２つの整数の和を返します

    Parameters
    ----------
    x : int
        整数そのいち
    y : int
        整数そのに

    Returns
    -------
    int
        ２つの値の合計
    """
    return x + y

def sample_product_int(x: int, y: int) -> int:
    """２つの整数の積を返します

    Parameters
    ----------
    x : int
        整数そのいち
    y : int
        整数そのに

    Returns
    -------
    int
        ２つの値の積
    """
    return x * y
